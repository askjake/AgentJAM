const fetchChatsData = async (
  incomingHeaders: AxiosIncomingClientHeaders['incomingHeaders'],
) => {
  const chatsResponse = await getChats({
    page: 1,
    limit: CHATS_PAGE_SIZE,
    incomingHeaders,
  });

  const {
    docs = [],
    hasNextPage = false,
    active_chat_id = null,
    totalDocs = 0,
  } = chatsResponse;

  return { docs, hasNextPage, active_chat_id, totalDocs };
};

/**
 * FIXED: Enhanced error handling for active chat fetching
 * 
 * This function now gracefully handles 404 errors when fetching an active chat.
 * Instead of propagating the error and causing the entire page to fail with a 404,
 * it catches the error and returns null, allowing the homepage to load successfully
 * with an empty/no active chat state.
 * 
 * Common scenarios this fixes:
 * - Chat was deleted but still referenced as active
 * - Chat ID stored in cookie/session is stale or invalid
 * - Race condition between chat deletion and page load
 * 
 * @param activeChatId - The chat ID to fetch
 * @param incomingHeaders - Request headers for authentication
 * @returns Chat object with messages, or null if chat not found or error occurred
 */
const fetchActiveChat = async ({
  activeChatId,
  incomingHeaders,
}: {
  activeChatId: string | undefined;
  incomingHeaders: AxiosIncomingClientHeaders['incomingHeaders'];
}) => {
  if (!activeChatId) return null;

  try {
    // Attempt to fetch the chat details
    const chat = await getChat({
      id: activeChatId,
      incomingHeaders,
    });

    if (!chat?.chat_id) return null;

    // Fetch messages for the chat
    const { docs = [], hasNextPage = false } = await getMessages({
      chat_id: activeChatId,
      page: 1,
      limit: CHAT_MESSAGES_PAGE_SIZE,
      incomingHeaders,
    });
    
    return {
      ...chat,
      messages: transformMessagesToObject(docs.reverse()),
      hasMoreMessages: hasNextPage,
      active: true,
    };
  } catch (error: any) {
    // FIXED: Gracefully handle 404 and other errors when fetching active chat
    // Log the error for debugging but don't let it crash the page
    const status = error?.response?.status;
    const chatId = activeChatId;
    
    console.warn('[Active Chat Fetch Warning]', {
      timestamp: new Date().toISOString(),
      chatId,
      errorType: status === 404 ? 'Chat Not Found' : 'Chat Fetch Error',
      status,
      message: error?.response?.data?.detail || error?.message,
      // This is expected behavior when:
      // - Chat was recently deleted
      // - User's active_chat_id reference is stale
      // - Backend and frontend are out of sync
      action: 'Continuing page load without active chat',
    });
    
    // Return null to indicate no active chat is available
    // The UI will handle this gracefully by showing an empty chat state
    return null;
  }
};

const fetchChatGroups = async (
  incomingHeaders: AxiosIncomingClientHeaders['incomingHeaders'],
) => {
  const { docs: chatsGroups = [] } = await getChatsGroups({
    page: 1,
    limit: CHAT_GROUPS_PAGE_SIZE,
    incomingHeaders,
  });

  return chatsGroups;
};

