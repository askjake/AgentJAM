export const getServerSideProps: GetServerSideProps<TemplateErrorProps> =
  wrapper.getServerSideProps(
    // @ts-ignore
    (store) =>
      // @ts-ignore
      async (ctx): Promise<GetServerSidePropsResult<TemplateErrorProps>> => {
        try {
          // Check if auth is disabled
          const disableAuth = process.env.NEXT_PUBLIC_DISABLE_AUTH === 'true';
          
          let userEmail = 
            ctx?.req?.cookies['userEmail'] ||
            ctx?.req?.headers['x-auth-request-email'];

          // If auth is disabled and no email, use a default test user
          if (disableAuth && !userEmail) {
            userEmail = 'test-user@dish.com';
            console.log('[NO-AUTH MODE] Using default test user:', userEmail);
          }

          if (!userEmail) {
            return {
              redirect: {
                destination: '/403',
                permanent: false,
              },
            };
          }

          const incomingHeaders: AxiosIncomingClientHeaders['incomingHeaders'] =
            {
              ...ctx.req.headers,
              'X-Auth-Request-Email': userEmail,
            };

          // Fetch user data and vault settings
          const { user, isVaultModeRegistered, isVaultModeEnabled, releases } =
            await fetchUserData(incomingHeaders);

          const releasesList = releases?.docs || [];

          // Dispatch user and vault data
          store.dispatch(setUser(user));
          store.dispatch(setVaultModeRegistered(isVaultModeRegistered));
          store.dispatch(setVaultMode(isVaultModeEnabled));
          store.dispatch(setReleases(releasesList));
          store.dispatch(setHasMoreReleases(!!releases?.hasNextPage));
          const lastRelease = releasesList[0];
          if (lastRelease) {
            const lastReleaseDate = user?.last_release_date;
            if (
              !lastReleaseDate ||
              customDayjs(lastRelease.date).isAfter(
                customDayjs(lastReleaseDate),
                'day',
              )
            ) {
              await updateLastReleaseDate({
                date: customDayjs(lastRelease.date).toDate(),
                incomingHeaders,
              });
              store.dispatch(setShowReleaseModal(true));
              store.dispatch(
                setUser({
                  ...user,
                  last_release_date: lastRelease.date,
                }),
              );
            }
          }
          // Fetch chats data
          const { docs, hasNextPage, active_chat_id, totalDocs } =
            await fetchChatsData(incomingHeaders);

          store.dispatch(
            setChats(
              docs.map((item) => ({
                ...item,
                active: active_chat_id === item.chat_id,
              })),
            ),
          );
          store.dispatch(setHasMoreChats(hasNextPage));
          store.dispatch(setTotalChats(totalDocs));
          const chatsGroups = await fetchChatGroups(incomingHeaders);
          // Dispatch chat groups
          store.dispatch(setChatsGroups(chatsGroups));
          
          // FIXED: Always attempt to fetch active chat, but don't let failures crash the page
          // The fetchActiveChat function now has internal error handling
          if (active_chat_id) {
            const activeChat = await fetchActiveChat({
              activeChatId: active_chat_id,
              incomingHeaders,
            });

            // Dispatch active chat if exists
            if (activeChat) {
              const { hasMoreMessages = false, ...chat } = activeChat;
              store.dispatch(setActiveChat(chat));
              store.dispatch(setHasMoreMessages(hasMoreMessages));
            } else {
              // FIXED: Explicitly set hasMoreMessages to false when no active chat
              // This ensures the UI is in a clean state
              store.dispatch(setHasMoreMessages(false));
            }
          } else {
            // No active_chat_id provided, ensure clean state
            store.dispatch(setHasMoreMessages(false));
          }

          return {
            props: {},
          };
        } catch (error: any) {
          // FIXED: Only critical errors (auth, user data, chats list) should trigger page-level errors
          // Individual chat fetch errors are now handled within fetchActiveChat
          return handleServerError({
            error,
            ctx,
          });
        }
      },
  );

export default HomePage;
